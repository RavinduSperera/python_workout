# for - limited and while - unlimited 

# display one range 
for i in range(10+1): # i - index
    print(i)

# display a specific range 
for i in range(10, 50+1): # no 1 - inclusive, no 2 - exclusive
    print(i)
    print('')

# we can change the x2 time 
for i in range(10, 50+1, 2):
    print(i)

for i in 'Ravindu perera':
    print(i)

